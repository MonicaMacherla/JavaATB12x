package com.ex_10_For_Loop;

public class Lab73_For_If_Loop {
    public static void main(String[] args) {
        for(int monica=0;monica<18;monica++){
            if(monica>15) {
                System.out.println("Iphone Gift");
            }
            else{
                System.out.println("No Gift");
            }
        }
    }
}
