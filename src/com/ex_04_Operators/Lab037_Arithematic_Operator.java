package com.ex_04_Operators;

public class Lab037_Arithematic_Operator {

    // / -> Quotient
    // % -> Remainder
    public static void main(String[] args){
        int i=30;
        int j=20;
        float k=3.21f;
        System.out.println(i+j);
        System.out.println(i-j);
        System.out.println(i*j);
        System.out.println(i/j);
        System.out.println(i/k);
        System.out.println(i%j);
        System.out.println("i+j");
    }

}
