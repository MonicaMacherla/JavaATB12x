package com.ex01.javabasics;

public class CommonMath {
    public static void main(String[] args){
        System.out.println(2+2);
        System.out.println(2*2);
        System.out.println(2-2);
        System.out.println(5/2); //prints Quotient
        System.out.println(5%2); //prints Remainder

    }
}
